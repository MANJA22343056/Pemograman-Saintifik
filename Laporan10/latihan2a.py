import numpy as np

# membuat dua array numpy 1D
arrayA = np.array([1,2,3,4,5])
arrayB = np.array([5,4,3,2,1])

# hitung hasil dari penjumlahan, perkalian, dan pengurangan dua array

penjumlahan = arrayA + arrayB
perkalian = arrayA * arrayB
pengurangan = arrayA - arrayB

print("penjumlahan arrayA dan arrayB:", penjumlahan)
print("perkalian arrayA dan arrayB:", perkalian)
print("pengurangan arrayA dan arrayB:", pengurangan)